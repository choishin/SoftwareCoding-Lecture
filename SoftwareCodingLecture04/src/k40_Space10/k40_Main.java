package k40_Space10;
//10.띄어쓰기 
public class k40_Main {
	
	public static void main(String[] args) {

		//반복 : 10번 
		for(int k40_i = 0; k40_i < 10; k40_i++) {
		//반복 : i개수보다 1번씩 적게 반복
		for(int k40_j = 0; k40_j < k40_i; k40_j++) {
			//띄어쓰기해주고
			System.out.printf(" "); 
		}
		//끝에 숫자적어주고 엔터
		System.out.printf("%d\n", k40_i);

		}
	}
}
	

